private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
try {
            String newFieldName = fieldNameTextField.getText();
            List<FieldFormatData> list = ((OutputFormat)parent).getFieldList();
            for(FieldFormatData fd : list){
                if(fd.getName().equals(newFieldName)){
                    JOptionPane.showMessageDialog(null, "Field Name already exists.");
                    return;
                }
            }
            FieldFormatData fd = new FieldFormatData(list.size()+1, newFieldName, "|");
            fd.setType(FieldFormatData.OVP_FIELD);            
            list.add(fd);            
            ((OutputFormat)parent).updateTable();
            parent.setEnabled(true);
            closeDialog(null);            
        } catch (Throwable t) {
            Log.quit(t);
        }
}//GEN-LAST:event_okButtonActionPerformed

