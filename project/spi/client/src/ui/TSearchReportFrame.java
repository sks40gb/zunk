   private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try {
         JFileChooser saveFile = new JFileChooser(); 
         
         FileFilter txtFilter = makeFilter("txt","Exported Data File");         
         saveFile.addChoosableFileFilter(txtFilter);
         saveFile.setFileFilter(txtFilter);
         
         FileFilter docFilter = makeFilter("doc","Exported Data File"); 
         saveFile.addChoosableFileFilter(docFilter);
         saveFile.setFileFilter(docFilter);
         
         int i = saveFile.showSaveDialog(this);
         
         String prefix = jLabel5.getText() + " = " + jLabel10.getText();
         String suffix = null;
         StringBuffer containExportData = new StringBuffer();
         if (isLocationSearch) {
            for (int j = 0; j < list.size(); j++) {
               String arr[] = list.get(j).toString().split(":");
               containExportData.append(arr[0] + "/" + field + arr[1] + "\n");
            }
            suffix = containExportData.toString();
         }
         else {
            suffix = jLabel6.getText() + " = " + jLabel11.getText();
         }

         if (i == JFileChooser.APPROVE_OPTION) {
            file = saveFile.getSelectedFile();                      
            path = file.getPath();
            
            if (file.exists()) {
               java.io.FileInputStream fis = new java.io.FileInputStream(path);
               byte[] readtheScript = new byte[fis.available()];
               fis.read(readtheScript);
               String newStringObj = new String(readtheScript);
               newStringObj = newStringObj.concat("\n" + prefix + "\n" + suffix);
               fis.close();

               java.io.FileOutputStream fos = new java.io.FileOutputStream(path);
               new PrintStream(fos).println(newStringObj);
               fos.close();
            }
            else {    
               if(saveFile.getFileFilter().equals(txtFilter)){
                  path = file.getPath() + ".txt";
               }
               else{
                  path = file.getPath() + ".doc";
               }
               FileOutputStream fout = new FileOutputStream(path);
               new PrintStream(fout).println(prefix + "\n" + suffix);
               fout.close();
            }
            JOptionPane.showMessageDialog(this, "Export done successfully!");
         }
         else {
         //JOptionPane.showMessageDialog(this,"No files selected !");
         }
      } catch (Exception e) {
         e.printStackTrace();
      }
   }//GEN-LAST:event_jButton1ActionPerformed

