   private void downloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downloadButtonActionPerformed
      
      if (projectCombo.getSelectedIndex() < 0) {
         JOptionPane.showMessageDialog(this, "Select any project !");
         projectCombo.requestFocus();
      }else if (volumeCombo.getSelectedIndex() < 0) {
         JOptionPane.showMessageDialog(this, "Select any volume !");
         volumeCombo.requestFocus();
      }else if (folderTextField.getText().trim().length() == 0) {
         folderTextField.requestFocus();
         JOptionPane.showMessageDialog(this, "Please specify the folder path");
      }      
      else {
          projectName = projectCombo.getSelectedItem().toString();
          volumeName = volumeCombo.getSelectedItem().toString();
         java.io.File file = new File(folderTextField.getText());
         if(file.exists() && file.isDirectory()){            
            folderTextField.setText(file.getPath());            
           downloadExportedFiles(folderTextField.getText());
         }
         else{
             JOptionPane.showMessageDialog(this,"Directory path doesn't exists !");
         }        
      }
      
}//GEN-LAST:event_downloadButtonActionPerformed

   private void downloadExportedFiles(String localDir) {
        try {            
            //get the Url connection
            String fileName = projectName+"_"+volumeName+".zip";
            String downloadPath = CommonProperties.getUploadFolderPath() + FOLDER_NAME +
                                                            File.separator + projectName + "_" + volumeName + File.separator + fileName;
            
            java.net.URL url = new URL(downloadPath);
            URLConnection conn = url.openConnection();
            InputStream in = conn.getInputStream();

            //file name with path where to write the file.
            String absoluteFileName = localDir + File.separator + fileName;

            File f = new File(absoluteFileName);
            if (!f.getParentFile().exists()) {
                f.getParentFile().mkdirs();
            }
            OutputStream out = new FileOutputStream(absoluteFileName);
            byte[] buf = new byte[1024];
            int len = 0;
            while ((len = in.read(buf)) > 0) {
                out.write(buf, 0, len);
            }
            // Close the streams
            out.close();
            Unzip unzip = new Unzip(this, localDir, fileName);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "TSearch Export has not been done for this volume.\nPlease contact administrator.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   
   
