    public void saveInitalState()
    {
        checkedRows = new ArrayList<String>();
        for (int i = 0; i < selectTable.getRowCount(); i++) {
            if (new Boolean(selectTable.getValueAt(i, 0).toString())) {
                checkedRows.add("true");
            } else {
                checkedRows.add("false");
            }
        }
    }

    public void resetInitialState()
    {
        for(int i=0; i<checkedRows.size(); i++)
        {            
            selectTable.setValueAt(new Boolean(checkedRows.get(i)), i, 0);
        }
    }
