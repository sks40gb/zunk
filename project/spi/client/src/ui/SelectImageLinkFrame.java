private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
    ArrayList selectedImageList = new ArrayList();
    ((FeedbackInvestigation)parentFrame).isSaved = false;
    for(int i = 0; i< selectTable.getRowCount(); i++)
    {
        if(new Boolean(selectTable.getValueAt(i, 0).toString()))
        {
            ((FeedbackInvestigation)parentFrame).selectedImageList.add(selectTable.getValueAt(i, 1).toString());
            selectedImageList.add(selectTable.getValueAt(i, 1).toString());
        }
    }
    (((FeedbackInvestigation)parentFrame).feedbackList.get(currentIndex)).setSelectedImageList(selectedImageList);//GEN-LAST:event_acceptButtonActionPerformed
    
    parentFrame.setEnabled(true);
    this.dispose();
}

